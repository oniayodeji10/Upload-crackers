<?php
// Include employeeDAO file
require_once('./dao/employeeDAO.php');
 
// Define variables and initialize with empty values
$name = $address = $salary = $date = "";
$name_err = $address_err = $salary_err =  $date_err = "";
$employeeDAO = new employeeDAO(); 

// Processing form data when form is submitted
if(isset($_POST["id"]) && !empty($_POST["id"])){
    // Get hidden input value
    $id = $_POST["id"];
    
    // Validate name
    $input_name = trim($_POST["name"]);
    if(empty($input_name)){
        $name_err = "Please enter a name.";
    } elseif(!filter_var($input_name, FILTER_VALIDATE_REGEXP, array("options"=>array("regexp"=>"/^[a-zA-Z\s]+$/")))){
        $name_err = "Please enter a valid name.";
    } else{
        $name = $input_name;
    }
    
    // Validate address address
    $input_address = trim($_POST["address"]);
    if(empty($input_address)){
        $address_err = "Please enter an address.";     
    } else{
        $address = $input_address;
    }
    
    // Validate salary
    $input_salary = trim($_POST["salary"]);
    if(empty($input_salary)){
        $salary_err = "Please enter the salary amount.";     
    } elseif(!ctype_digit($input_salary)){
        $salary_err = "Please enter a positive integer value.";
    } else{
        $salary = $input_salary;
    }

    //Validate Date
    //var_dump(checkdate($month, $day, $year));
    $input_date = trim($_POST["date"]);
    if(empty($input_date)){
        $date_err = "Please enter the correct date."; 
    } else{
        $date = $input_date;
    }
    
    // Check input errors before inserting in database
    if(empty($name_err) && empty($address_err) && empty($salary_err)  && empty($date_err)){   
        $employee = new Employee($id, $name, $address, $salary, $date);
        $result = $employeeDAO->updateEmployee($employee);
        echo '<br><h6 style="text-align:center">' . $result . '</h6>';   
        header( "refresh:2; url=index.php" ); 
        // Close connection
        $employeeDAO->getMysqli()->close();
    }

} else{
    // Check existence of id parameter before processing further
    if(isset($_GET["id"]) && !empty(trim($_GET["id"]))){
        // Get URL parameter
        $id =  trim($_GET["id"]);
        $employee = $employeeDAO->getEmployee($id);
                
        if($employee){
            // Retrieve individual field value
            $name = $employee->getName();
            $address = $employee->getAddress();
            $salary = $employee->getSalary();
            $date = $employee->getDate();

        } else{
            // URL doesn't contain valid id. Redirect to error page
            header("location: error.php");
            exit();
        }
    } else{
        // URL doesn't contain id parameter. Redirect to error page
        header("location: error.php");
        exit();
    }
    // Close connection
    $employeeDAO->getMysqli()->close();
}
